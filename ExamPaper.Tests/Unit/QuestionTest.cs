    /// <summary>
    /// Тесты конструктора с автоматической генерацией Id (Version 7).
    /// </summary>
    public class ConstructorWithAutoIdTests
    {
        /// <summary>
        /// Проверяет, что конструктор с текстом вопроса создаёт экземпляр
        /// с корректным текстом и непустым идентификатором.
        /// </summary>
        [Fact]
        public void WithValidText_CreatesQuestionWithAutoGeneratedId()
        {
            // Arrange
            var text = "What is SOLID?";

            // Act
            var question = new Question(text);

            // Assert
            Assert.NotEqual(Guid.Empty, question.Id);
            Assert.Equal(text, question.Text);
        }

        /// <summary>
        /// Проверяет, что конструктор выбрасывает <see cref="ArgumentException"/>
        /// при передаче пустого текста вопроса.
        /// </summary>
        /// <param name="invalidText">Невалидный текст вопроса.</param>
        [Theory]
        [InlineData("")]
        [InlineData("   ")]
        [InlineData(null)]
        public void WithNullOrWhiteSpaceText_ThrowsArgumentException(string? invalidText)
        {
            // Act & Assert
            var exception = Assert.Throws<ArgumentException>(() => new Question(invalidText));

            Assert.Equal("text", exception.ParamName);
            Assert.Contains("Текст вопроса не может быть пустым", exception.Message);
        }

        /// <summary>
        /// Проверяет, что конструктор автоматически обрезает пробелы в начале и конце текста.
        /// </summary>
        [Fact]
        public void WithTextContainingWhitespace_TrimsTheText()
        {
            // Arrange
            var textWithSpaces = "  Hello World  ";
            var expectedText = "Hello World";

            // Act
            var question = new Question(textWithSpaces);

            // Assert
            Assert.Equal(expectedText, question.Text);
        }

        /// <summary>
        /// Проверяет, что каждый новый вопрос получает уникальный идентификатор.
        /// </summary>
        [Fact]
        public void MultipleQuestions_GetDifferentIds()
        {
            // Act
            var question1 = new Question("Question 1");
            var question2 = new Question("Question 2");

            // Assert
            Assert.NotEqual(question1.Id, question2.Id);
        }
    }

